// 領域の数
// 無限に広い平面の上に、無限に長い直線を数本引くと、この平面はいくつかの領域に分割されます。たとえば、直線を１本引くと、平面は２つの領域に分割されます。同じ数の直線を引いても、引き方によって得られる領域の数は異なります。たとえば、2 本の直線を平行に引けば得られる領域は 3 つになり、互いに垂直に引けば得られる領域は 4 つになります。

// n 本の直線を引くことで得られる最大の領域の数を出力するプログラムを作成してください。

// Input
// 複数のデータセットが与えられます。各データセットに n (1 ≤ n ≤ 10,000) が１行に与えられます。入力の最後まで処理して下さい。

// データセットの数は 50 を超えません。

// Output
// 各データセットに対して、最大の分割数を１行に出力して下さい。

#include <bits/stdc++.h>
using namespace std;

int main() {
    int n;
    int cnt[10001] = {};
    cnt[1] = 2;
    for (int i = 2; i <= 10000; i++) {
        cnt[i] = cnt[i-1] + i;
    }
    while (cin >> n) {
        cout << cnt[n] << endl;
    }
    return 0;
}